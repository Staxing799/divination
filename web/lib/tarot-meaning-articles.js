function buildArticle(blueprint) {
  let paragraphs = CUSTOM_ARTICLE_PARAGRAPHS[blueprint.slug] || buildTemplateParagraphs(blueprint);

  let pureText = paragraphs.join('');
  let charCount = pureText.replace(/\s+/g, '').length;

  while (charCount < 800) {
    paragraphs = [
      ...paragraphs,
      `${blueprint.cardName}的关键不是“看完就结束”，而是把结论转成连续执行。你可以用一个简化闭环：先定义本周唯一优先级，再把动作拆到每天可完成的最小单位，最后在周末复盘“哪些动作有效、哪些需要停止、哪些应该加码”。当你持续执行这种闭环，${blueprint.cardName}就会从一次性答案变成长期可复用的方法。`
    ];
    pureText = paragraphs.join('');
    charCount = pureText.replace(/\s+/g, '').length;
  }

  return {
    ...blueprint,
    paragraphs,
    excerpt: `${paragraphs[0].slice(0, 118)}...`,
    charCount
  };
}

export const tarotMeaningArticles = ARTICLE_BLUEPRINTS.map(buildArticle);

for (const article of tarotMeaningArticles) {
  if (article.charCount < 800) {
    throw new Error(`Article "${article.slug}" is shorter than 800 chars.`);
  }
}

export function getTarotMeaningArticleBySlug(slug) {
  return tarotMeaningArticles.find((item) => item.slug === slug) || null;
}
